package class2;

public class OperatoresDemo {
    public static void main(String[] args) {
        int number1=10;
        int number2=20;
        System.out.println(number1+number2);

        //ctrl+D is duplicate the line
        System.out.println(number1-number2);
        System.out.println(number1*number2);
        System.out.println(number1/number2);
        /*
        if you have to store whole number always use int
        if you have to store decimal numbers always use double
        if you have to store characters always use string
         */

    }
}
